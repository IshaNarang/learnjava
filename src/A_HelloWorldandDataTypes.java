public class A_HelloWorldandDataTypes {

    public int  myNum ;

    public static void main(String[] args) {

        System.out.println("Hello World!");

        int myNum = 888888888;

        System.out.println("My number is " + myNum);

        short myshort = 32767;
        System.out.println("My short is " + myshort);

        long myLong = 77777777444566L;
        System.out.println("My long is " + myLong);

        double myDouble = 555544.4554444;
        System.out.println("My double is " + myDouble);

        float myfloat = 33.6f;
        System.out.println("My float is " + myfloat);

        char myChar = 's'+'a';
        System.out.println("My char is " + myChar);

        boolean mybool = false;
        System.out.println("My boolean is " + mybool);

        byte myByte = 127;
        System.out.println("My byte is " + myByte);


    }


}
